package io.github.oliviercailloux.students_project_following.spreadsheet;

public class SpreadsheetException extends Exception {

	private static final long serialVersionUID = 1L;

	public SpreadsheetException() {
		super();
	}

	public SpreadsheetException(String arg0) {
		super(arg0);
	}

	public SpreadsheetException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SpreadsheetException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public SpreadsheetException(Throwable arg0) {
		super(arg0);
	}

}
